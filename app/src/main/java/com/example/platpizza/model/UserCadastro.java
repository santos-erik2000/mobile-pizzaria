package com.example.platpizza.model;

public class UserCadastro {
    private String nomeVM;
    private String cpfVM;
    private String apelidoVM;
    private String senhaVM;
    private String emailVM;
    private String telefoneVM;

    public String getNomeVM() {
        return nomeVM;
    }

    public void setNomeVM(String nomeVM) {
        this.nomeVM = nomeVM;
    }

    public String getCpfVM() {
        return cpfVM;
    }

    public void setCpfVM(String cpfVM) {
        this.cpfVM = cpfVM;
    }

    public String getApelidoVM() {
        return apelidoVM;
    }

    public void setApelidoVM(String apelidoVM) {
        this.apelidoVM = apelidoVM;
    }

    public String getSenhaVM() {
        return senhaVM;
    }

    public void setSenhaVM(String senhaVM) {
        this.senhaVM = senhaVM;
    }

    public String getEmailVM() {
        return emailVM;
    }

    public void setEmailVM(String emailVM) {
        this.emailVM = emailVM;
    }

    public String getTelefoneVM() {
        return telefoneVM;
    }

    public void setTelefoneVM(String telefoneVM) {
        this.telefoneVM = telefoneVM;
    }
}
